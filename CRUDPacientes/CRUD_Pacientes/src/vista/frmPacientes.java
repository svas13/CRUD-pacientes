    private void cbxModificarBuscarPacItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxModificarBuscarPacItemStateChanged
        try {
            paciente Paciente = (paciente) cbxModificarBuscarPac.getSelectedItem();
            cbxTipoDocModificarPac.setSelectedItem(Paciente.getTipoDoc().toString());
            txtDocumentoModificarPac.setText(String.valueOf(Paciente.getNumeroDocumento()));
            txtNombresModificarPac.setText(Paciente.getNombres());
            txtApellidoModificarPac.setText(Paciente.getApellidos());
            txtTelefonoModificarPac.setText(String.valueOf(Paciente.getTelefono()));
            txtDireccionModificarPac.setText(Paciente.getDireccion());
            txtEmailModificarPac.setText(Paciente.getEmail());
            cbxEstadoModificarPac.setSelectedItem(Paciente.isEstado() ? "Activo" : "Inactivo");
            this.idTempo = Paciente.getId();
            pnModificarDatosPac.setVisible(true);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_cbxModificarBuscarPacItemStateChanged

