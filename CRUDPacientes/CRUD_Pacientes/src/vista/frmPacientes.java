private boolean validarDatos (){
        if(cbxTipoDocCrearPac.getSelectedItem().toString().isEmpty() || txtDocumentoCrearPac.getText().isEmpty() || txtNombresCrearPac.getText().isEmpty() ||  txtApellidoCrearPac.getText().isEmpty() || txtTelefonoCrearPac.getText().isEmpty() || txtDireccionCrearPac.getText().isEmpty() || txtEmailCrearPac.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Es necesario diligenciar todos los campos", "Datos No Validos", JOptionPane.ERROR_MESSAGE);
            return false;
        }else{
            return true;
        }
    }

